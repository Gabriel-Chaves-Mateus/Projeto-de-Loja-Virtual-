    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        
        lojabo = new lojaBO();
        lojaDTO lojadto = new lojaDTO();
        lojadto.setPedido(Integer.parseInt(lbPedido.getText()));
        lojadto.setData(Data.getText());
        lojadto.setNome(txtNome.getText());
        lojadto.setSobrenome(txtSobrenome.getText());
        lojadto.setEndereco(txtEndereco.getText());
        lojadto.setSexo(String.valueOf(cbxSexo.getSelectedItem()));
        lojadto.setTelefone(txtTelefone.getText());
        lojadto.setEmail(txtEmail.getText());
        lojadto.setNome(txtNome.getText());
        lojadto.setAssunto(String.valueOf(cbxComentario.getSelectedItem()));
        lojadto.setComentario(txtComentario.getText());
        
        if(lojabo.inserir(carregarDTO())){
            JOptionPane.showMessageDialog(null, "Dados inseridos com sucesso!");
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao inserir os dados");
        }
        
        
        
        
        Date data = new Date();
        SimpleDateFormat formatar = new SimpleDateFormat("dd 'de' MMMMM 'de' yyyy");
        String dataFormada = formatar.format(data);
       
        Data.setText(dataFormada);
        
        
        
        DefaultTableModel model =(DefaultTableModel) table.getModel();
        
        model.addRow(new Object[]{
            nrPedido++,
            lojadto.getData(),
            lojadto.getNome(),
            lojadto.getSobrenome(),
            lojadto.getEndereco(),
            lojadto.getSexo(),
            lojadto.getTelefone(),
            lojadto.getEmail(),
            lojadto.getAssunto(),
            lojadto.getComentario()
        });
        
         lbPedido.setText(""+nrPedido);
        
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

