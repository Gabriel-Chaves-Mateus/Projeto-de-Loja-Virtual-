    public lojaDTO carregarDTO(){
        lojaDTO lojadto = new lojaDTO();
        if(lbPedido.getText().equals(""))
            lojadto.setPedido(0);
        else
            lojadto.setPedido(Integer.parseInt(lbPedido.getText()));
            lojadto.setData(Data.getText());
            lojadto.setNome(txtNome.getText());
            lojadto.setSobrenome(txtSobrenome.getText());
            lojadto.setEndereco(txtEndereco.getText());
            lojadto.setSexo(String.valueOf(cbxSexo.getSelectedItem()));
            lojadto.setTelefone(txtTelefone.getText());
            lojadto.setEmail(txtEmail.getText());
            lojadto.setAssunto(String.valueOf(cbxComentario.getSelectedItem()));
            lojadto.setComentario(txtComentario.getText());
        return lojadto;
    
    }
    public void carregarTela(lojaDTO loja){
        lbPedido.setText(String.valueOf(loja.getPedido()));
        Data.setText(loja.getData());
        txtNome.setText(loja.getNome());
        txtSobrenome.setText(loja.getSobrenome());
        txtEndereco.setText(loja.getEndereco());
        cbxSexo.setSelectedItem(String.valueOf(loja.getSexo()));
        txtTelefone.setText(loja.getTelefone());
        txtEmail.setText(loja.getEmail());
        cbxComentario.setSelectedItem(String.valueOf(loja.getAssunto()));
        txtComentario.setText(loja.getComentario());
    }
    
